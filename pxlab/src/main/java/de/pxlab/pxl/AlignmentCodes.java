package de.pxlab.pxl;

/**
 * Codes for defining alignment.
 * 
 * @author H. Irtel
 * @version 0.1.0
 * @see TextElement
 * @see TextParagraphElement
 */
public interface AlignmentCodes {
	/**
	 * Text alignment code for left aligned lines in a multiline paragraph.
	 */
	public static final int LEFT = 0;
	/**
	 * Text alignment code for centered lines in a multiline paragraph.
	 */
	public static final int CENTER = 1;
	/**
	 * Text alignment code for right aligned lines in a multiline paragraph.
	 */
	public static final int RIGHT = 2;
}
