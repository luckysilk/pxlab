package de.pxlab.pxl;

/**
 * Codes for controlling a media recorder.
 * 
 * @author H. Irtel
 * @version 0.1.0
 */
public interface RecorderCodes {
	/** Start recording. */
	public static final int REC_START = 1;
	/** Stop recording. */
	public static final int REC_STOP = 2;
	/** Pause recording. */
	public static final int REC_PAUSE = 3;
	/** Continue recording. */
	public static final int REC_CONTINUE = 4;
}
